const language = '';

switch (language) {
    case 'RU':
        console.log('Здравствуйте!');
        break;
    case 'EN':
        console.log('Hello!');
        break;
    case 'FR':
        console.log('Bonjour!');
        break;
    case 'DE':
        console.log('Guten Tag!');
        break;
    case 'CN':
        console.log('你好!');
        break;
    default:
        console.log('Язык пользователя не определен!');
}